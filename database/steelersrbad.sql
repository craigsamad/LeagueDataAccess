DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS board_posts;
DROP TABLE IF EXISTS owner_score;
DROP TABLE IF EXISTS game;
DROP TABLE IF EXISTS owner;

CREATE TABLE users (
  id serial PRIMARY KEY,
  username varchar(255) NOT NULL UNIQUE,
  password varchar(32) NOT NULL,
  salt varchar(256) NOT NULL,
  role varchar(255) NOT NULL default('user')
);

CREATE TABLE board_posts (
  id serial,
  poster varchar(40) NOT NULL,
  post_text varchar(256) NOT NULL
);

CREATE TABLE owner (
	owner_id serial,
	first_name varchar(40) NOT NULL,          
	last_name varchar(40) NOT NULL,
  
	CONSTRAINT pk_owner_owner_id PRIMARY KEY (owner_id)
);

CREATE TABLE game (
	game_id serial,
	season integer NOT NULL,
	week_num integer NOT NULL,
	type integer NOT NULL,
	
	CONSTRAINT pk_game_game_id PRIMARY KEY (game_id)
);

CREATE TABLE owner_score (
	row_id serial,
	game_id integer,
	owner_id integer,
	score float,
	
	CONSTRAINT pk_owner_score_row_id PRIMARY KEY (row_id)
);

